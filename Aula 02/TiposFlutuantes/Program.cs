//Declarando Tipos Flutuantes

double n1 = 1.234;

float n2 = 1.234f;

decimal n3 = 1.234m;

System.Console.WriteLine(n1);
System.Console.WriteLine(n2);
System.Console.WriteLine(n3);

float x = 1f / 3f;

double y = 1d / 3d;

decimal z = 1m / 3m;

System.Console.WriteLine(x);
System.Console.WriteLine(y);
System.Console.WriteLine(z);

//Declarando Tipos Não Numéricos

bool ativo = true;
bool inativo = false;

System.Console.WriteLine(ativo);
System.Console.WriteLine(inativo);

int a = 10;
int b = 15;

System.Console.WriteLine(a > b);